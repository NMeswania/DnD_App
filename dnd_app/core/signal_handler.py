import signal
import sys

###################################################################################################
###################################################################################################
###################################################################################################


class SignalHandler:

  def __init__(self, main_task):
    self._main_task = main_task

###################################################################################################

  def Assign(self):
    signal.signal(signal.SIGINT, self._HandleSignal)

###################################################################################################

  def _HandleSignal(self, sig, frame):
    del self._main_task
    sys.exit(0)


