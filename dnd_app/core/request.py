import uuid

###################################################################################################
###################################################################################################
###################################################################################################


class Request:

  def __init__(self, type: str = "", value: str = ""):
    self.data = {"type": type, "value": value}
    self.id_ = uuid.uuid4()

###################################################################################################

  def id(self) -> str:
    return str(self.id_)


