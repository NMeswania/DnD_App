import uuid

###################################################################################################
###################################################################################################
###################################################################################################


class Request:

  def __init__(self, type: str = "", value: str = ""):
    self._data = {'type': type, 'value': value}
    self._id = uuid.uuid4()

###################################################################################################

  def type(self):
    return self._data['type']

###################################################################################################

  def value(self):
    return self._data['value']

###################################################################################################

  def id(self) -> str:
    return str(self._id)

###################################################################################################

  def __str__(self):
    s = "Request:"
    s += f"\n\tID: {self.id()}"
    s += "\n\tData:"
    s += f"\n\t\tType: {self._data['type']}"
    s += f"\n\t\tValue: {self._data['value']}"
    return s


